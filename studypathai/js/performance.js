// ── Delete Handler ───────────────────────────────────────────
async function handleDeleteResult(id) {
  if (!confirm("Delete this performance result? The auto-generated tasks and assignments will remain, but you can delete them manually.")) return;
  try {
    await deletePerformanceResult(id);
    showSuccess("Performance result deleted.");
  } catch (err) {
    showError("Failed to delete. Please try again.");
    console.error("deletePerformanceResult error:", err);
  }
}

// ── Form Submit ──────────────────────────────────────────────
async function handlePerformanceSubmit(e) {
  e.preventDefault();

  var subject    = (document.getElementById("perf-subject").value || "").trim();
  var topic      = (document.getElementById("perf-topic").value   || "").trim();
  var scoreRaw   = document.getElementById("perf-score").value;
  var maxRaw     = document.getElementById("perf-max").value;
  var confidence = document.getElementById("perf-confidence").value;
  var examDate   = document.getElementById("perf-exam-date").value || null;
  var notes      = (document.getElementById("perf-notes").value   || "").trim();
  var errEl      = document.getElementById("perf-form-error");
  var btn        = document.getElementById("perf-submit-btn");

  // Validation
  errEl.textContent = "";
  clearFieldErrors();
  var valid = true;

  if (!subject) { setFieldError("perf-subject", "Subject is required."); valid = false; }
  if (!topic)   { setFieldError("perf-topic",   "Topic is required.");   valid = false; }

  var score    = parseFloat(scoreRaw);
  var maxScore = parseFloat(maxRaw);

  if (scoreRaw === "" || isNaN(score))                  { setFieldError("perf-score", "Score is required and must be a number.");       valid = false; }
  if (maxRaw  === "" || isNaN(maxScore) || maxScore <=0){ setFieldError("perf-max",   "Max Score must be a number greater than 0."); valid = false; }
  if (valid && score > maxScore)                        { setFieldError("perf-score", "Score cannot be greater than Max Score.");       valid = false; }
  if (!valid) return;

  var analysis = calculatePerformanceAnalysis(score, maxScore);

  var resultData = {
    subject:                   subject,
    topic:                     topic,
    score:                     score,
    maxScore:                  maxScore,
    percentage:                analysis.percentage,
    confidence:                confidence || "Medium",
    examDate:                  examDate,
    notes:                     notes,
    weaknessLevel:             analysis.weaknessLevel,
    priority:                  analysis.priority,
    recommendedDailyTime:      analysis.recommendedDailyTime,
    recommendedWeeklyTime:     analysis.recommendedWeeklyTime,
    generatedStudyTaskCount:   analysis.taskCount,
    generatedAssignmentCount:  analysis.assignCount,
    skillUpdated:              true,
    source:                    "manual-performance-entry",
  };

  showLoading(btn, "Analysing…");
  var analysisBox = document.getElementById("analysis-result");
  if (analysisBox) analysisBox.style.display = "none";

  try {
    // 1. Save the performance result
    var resultId = await savePerformanceResult(resultData);

    // 2. Generate study tasks
    var taskCount = await generateTasksForResult(resultData, resultId);

    // 3. Generate assignments
    var assignCount = await generateAssignmentsForResult(resultData, resultId);

    // 4. Upsert skill
    await upsertSkillFromResult(resultData);

    showSuccess("Analysis complete! Study plan generated.");
    showAnalysisResult(resultData, taskCount, assignCount);
    document.getElementById("perf-form").reset();
  } catch (err) {
    console.error("handlePerformanceSubmit error:", err);
    errEl.textContent = "Failed to save. Please check your connection and try again.";
  } finally {
    hideLoading(btn);
  }
}

// ── Field Error Helpers ──────────────────────────────────────
function setFieldError(fieldId, msg) {
  var el = document.getElementById(fieldId);
  if (el) el.classList.add("error");
  var errEl = document.getElementById(fieldId + "-error");
  if (errEl) errEl.textContent = msg;
}

function clearFieldErrors() {
  document.querySelectorAll(".perf-form-field.error, .form-control.error").forEach(function(el) {
    el.classList.remove("error");
  });
  ["perf-subject","perf-topic","perf-score","perf-max"].forEach(function(id) {
    var errEl = document.getElementById(id + "-error");
    if (errEl) errEl.textContent = "";
  });
}

// ── Skeleton ─────────────────────────────────────────────────
function buildSkeletonHTML() {
  var html = "";
  for (var i = 0; i < 3; i++) {
    html += '<div class="perf-result-card">' +
      '<div class="skeleton" style="height:1rem;width:55%;margin-bottom:.75rem;"></div>' +
      '<div class="skeleton" style="height:.75rem;width:35%;margin-bottom:.5rem;"></div>' +
      '<div class="skeleton" style="height:6px;width:100%;border-radius:4px;"></div>' +
    '</div>';
  }
  return html;
}

// ── Escape HTML ──────────────────────────────────────────────
function escHtmlP(str) {
  return String(str || "").replace(/&/g,"&amp;").replace(/</g,"&lt;").replace(/>/g,"&gt;").replace(/"/g,"&quot;");
}

